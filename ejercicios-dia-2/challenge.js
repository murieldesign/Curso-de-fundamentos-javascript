// Pedir al usuario dos números
let num1 = Number(prompt("Ingresa el primer número:"));
let num2 = Number(prompt("Ingresa el segundo número:"));

// Comparaciones
console.log("Número 1:", num1);
console.log("Número 2:", num2);
console.log("¿Son iguales? ", num1 == num2);
console.log("¿Son estrictamente iguales? ", num1 === num2);
console.log("¿El primer número es mayor? ", num1 > num2);
console.log("¿El primer número es menor? ", num1 < num2);
console.log("¿Son diferentes? ", num1 != num2);

