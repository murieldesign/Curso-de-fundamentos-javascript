 // Muestra los números del 1 al 5 usando un bucle for
for (let i = 1; i <= 5; i++) {
  console.log("Número:", i);
}
// Cuenta regresiva del 3 al 1 con un while
let contador = 3;
while (contador > 0) {
  console.log("Cuenta atrás:", contador);
  contador--; // resta 1 al siguiente elemento hasta que el valor sea mayor que cero
}
